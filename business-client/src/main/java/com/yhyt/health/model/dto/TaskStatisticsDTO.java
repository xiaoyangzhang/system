package com.yhyt.health.model.dto;

import java.io.Serializable;

public class TaskStatisticsDTO implements Serializable {

    private static final long serialVersionUID = 746415768710828634L;
    private Integer noDealCount;
    private Integer dealingCount;
    private Integer dealedCount;

    public Integer getNoDealCount() {
        return noDealCount;
    }

    public void setNoDealCount(Integer noDealCount) {
        this.noDealCount = noDealCount;
    }

    public Integer getDealingCount() {
        return dealingCount;
    }

    public void setDealingCount(Integer dealingCount) {
        this.dealingCount = dealingCount;
    }

    public Integer getDealedCount() {
        return dealedCount;
    }

    public void setDealedCount(Integer dealedCount) {
        this.dealedCount = dealedCount;
    }
}
