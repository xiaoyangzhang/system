package com.yhyt.health.model.vo;

import java.util.Date;

/**
 * 消息body
 *
 * @author gsh
 * @create 2017-10-14 16:14
 **/
public class XmppMessageBody {

    private String isRead;

    private String fromUserName;

    private String fromUserId;

    private String appointmentInfo;//”a:b:c”, a ：1 预约 2 转诊    b ：id   c：当前状态

    private String afterInfo;//”a:b”, a ：1诊后咨询 2 随访    b ：2发起 3对话 4 拒绝  5结束

    private String content;

    private Date timeSend;

    private String type;

    private String token;

    private Long patientId;

    public String getIsRead() {
        return isRead;
    }

    public void setIsRead(String isRead) {
        this.isRead = isRead;
    }

    public String getFromUserName() {
        return fromUserName;
    }

    public void setFromUserName(String fromUserName) {
        this.fromUserName = fromUserName;
    }

    public String getFromUserId() {
        return fromUserId;
    }

    public void setFromUserId(String fromUserId) {
        this.fromUserId = fromUserId;
    }

    public String getAppointmentInfo() {
        return appointmentInfo;
    }

    public void setAppointmentInfo(String appointmentInfo) {
        this.appointmentInfo = appointmentInfo;
    }

    public String getAfterInfo() {
        return afterInfo;
    }

    public void setAfterInfo(String afterInfo) {
        this.afterInfo = afterInfo;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public Date getTimeSend() {
        return timeSend;
    }

    public void setTimeSend(Date timeSend) {
        this.timeSend = timeSend;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public Long getPatientId() {
        return patientId;
    }

    public void setPatientId(Long patientId) {
        this.patientId = patientId;
    }
}
