package com.yhyt.health.constant;

import java.io.Serializable;

public class Dictionary implements Serializable {

    private int code;
    private String name;

    public Dictionary(int code, String name) {
        this.code = code;
        this.name = name;
    }

    public static final Dictionary DICT_TITLE=new Dictionary(1,"职称");
    public static final Dictionary DICT_DUTY=new Dictionary(2,"职务");
    public static final Dictionary DICT_EDUCATION=new Dictionary(3,"学历");
    public static final Dictionary DICT_HOSPITAL_LEVEL=new Dictionary(4,"医院级别");

    public int getCode() {
        return code;
    }

    public String getName() {
        return name;
    }
}
